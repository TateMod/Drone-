import machine
import time

# The 91-byte configuration block
VL51L1X_DEFAULT_CONFIGURATION = bytes([
    0x00, 0x00, 0x00, 0x01, 0x02, 0x00, 0x02, 0x08, 0x00, 0x08, 0x10, 0x01,
    0x01, 0x00, 0x00, 0x00, 0x00, 0xff, 0x00, 0x0F, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x20, 0x0b, 0x00, 0x00, 0x02, 0x0a, 0x21, 0x00, 0x00, 0x05, 0x00,
    0x00, 0x00, 0x00, 0xc8, 0x00, 0x00, 0x38, 0xff, 0x01, 0x00, 0x08, 0x00,
    0x00, 0x01, 0xdb, 0x0f, 0x01, 0xf1, 0x0d, 0x01, 0x68, 0x00, 0x80, 0x08,
    0xb8, 0x00, 0x00, 0x00, 0x00, 0x0f, 0x89, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x01, 0x0f, 0x0d, 0x0e, 0x0e, 0x00, 0x00, 0x02, 0xc7, 0xff,
    0x9B, 0x00, 0x00, 0x00, 0x01, 0x01, 0x40
])

class VL53L1X:
    def __init__(self, i2c, address=0x29):
        self.i2c = i2c
        self.address = address
        time.sleep_ms(100) # Wait for sensor boot
        
        # Checking for 0xEAAA (Standard ID)
        val = self.read_model_id()
        print(f"Detected Model ID: {hex(val)}")
        
        # Load configuration starting at 0x002D
        buf = bytes([(0x002D >> 8) & 0xFF, 0x002D & 0xFF]) + VL51L1X_DEFAULT_CONFIGURATION
        self.i2c.writeto(self.address, buf)
        
        # Start ranging
        self.writeReg(0x0087, 0x40) 

    def writeReg(self, reg, value):
        self.i2c.writeto(self.address, bytes([(reg >> 8) & 0xFF, reg & 0xFF, value]))

    def readReg(self, reg):
        self.i2c.writeto(self.address, bytes([(reg >> 8) & 0xFF, reg & 0xFF]))
        return self.i2c.readfrom(self.address, 1)[0]

    def readReg16Bit(self, reg):
        self.i2c.writeto(self.address, bytes([(reg >> 8) & 0xFF, reg & 0xFF]))
        data = self.i2c.readfrom(self.address, 2)
        return (data[0] << 8) + data[1]

    def read_model_id(self):
        return self.readReg16Bit(0x010F)

    def read(self):
        # Wait for data ready bit
        while not (self.readReg(0x0089) & 0x01):
            time.sleep_ms(1)
        
        # Read distance (High byte 0x0096, Low byte 0x0097)
        hi = self.readReg(0x0096)
        lo = self.readReg(0x0097)
        
        # Clear interrupt to trigger next measurement
        self.writeReg(0x0086, 0x01)
        return (hi << 8) | lo


